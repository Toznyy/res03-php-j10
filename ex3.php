function trouveLe1(array $map) : array {
    $le1 = [];
    $count1 = count($map);
    for ($i = 0; $i < $count1; $i++) {
        $count2 = count($map[$i]);
        for ($j = 0; $j < $count2; $j++) {
            if ($map[$i][$j] === "1") {
                $x1 = $i;
                $y1 = $j;
                $le1[] = $x1;
                $le1[] = $y1;
            }
        }
    }
    return $le1;
}

function trouveLe2(array $map) : array {
    $le2 = [];
    $count1 = count($map);
    for ($i = 0; $i < $count1; $i++) {
        $count2 = count($map[$i]);
        for ($j = 0; $j < $count2; $j++) {
            if ($map[$i][$j] === "2") {
                $x2 = $i;
                $y2 = $j;
                $le2[] = $x2;
                $le2[] = $y2;
            }
        }
    }
    return $le2;
}

function chemin(array $map) : array {
    $chemin = [];
    $count = 0;
    $le1 = trouveLe1($map);
    $le2 = trouveLe2($map);
    while ($le1 !== $le2) {
        if ($le1[0] < $le2[0]) {
            $le1[0]+=1;
